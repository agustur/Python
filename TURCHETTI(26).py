print ("Mostrar si el numero ingresado es Positivo, Negativo o 0")

num = int(input("\nIngrese su numero: "))

if num > 0:
    print ("El numero es positivo")
elif num == 0:
    print ("El numero es 0")
else:
    print ("El numero es negativo")

