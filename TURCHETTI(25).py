print ("Ingresar un numero y mostrar si esta entre el 50 y el 100")

n = int(input("\nIngrese el numero: "))

if n >= 50 and n <= 100:
    print ("\nSu numero esta entre los valores (50-100)")

else:
    print ("\nSu numero no esta entre los valores (50-100)")

